package org.zhadaev.energy.model;

import java.util.Date;

public class Form {

    private Date date;
    private String income;
    private String energyIncome;
    private String energyPrice;
    private String energyVolume;
    private String energyIncomeResale;
    private String energyIncomeResalePrice;
    private String energyIncomeResaleVolume;
    private String buyIncomeResale;
    private String buyIncomeResalePrice;
    private String buyIncomeResaleVolume;

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public String getIncome() {
        return income;
    }

    public void setIncome(String income) {
        this.income = income;
    }

    public String getEnergyIncome() {
        return energyIncome;
    }

    public void setEnergyIncome(String energyIncome) {
        this.energyIncome = energyIncome;
    }

    public String getEnergyPrice() {
        return energyPrice;
    }

    public void setEnergyPrice(String energyPrice) {
        this.energyPrice = energyPrice;
    }

    public String getEnergyVolume() {
        return energyVolume;
    }

    public void setEnergyVolume(String energyVolume) {
        this.energyVolume = energyVolume;
    }

    public String getEnergyIncomeResale() {
        return energyIncomeResale;
    }

    public void setEnergyIncomeResale(String energyIncomeResale) {
        this.energyIncomeResale = energyIncomeResale;
    }

    public String getEnergyIncomeResalePrice() {
        return energyIncomeResalePrice;
    }

    public void setEnergyIncomeResalePrice(String energyIncomeResalePrice) {
        this.energyIncomeResalePrice = energyIncomeResalePrice;
    }

    public String getEnergyIncomeResaleVolume() {
        return energyIncomeResaleVolume;
    }

    public void setEnergyIncomeResaleVolume(String energyIncomeResaleVolume) {
        this.energyIncomeResaleVolume = energyIncomeResaleVolume;
    }

    public String getBuyIncomeResale() {
        return buyIncomeResale;
    }

    public void setBuyIncomeResale(String buyIncomeResale) {
        this.buyIncomeResale = buyIncomeResale;
    }

    public String getBuyIncomeResalePrice() {
        return buyIncomeResalePrice;
    }

    public void setBuyIncomeResalePrice(String buyIncomeResalePrice) {
        this.buyIncomeResalePrice = buyIncomeResalePrice;
    }

    public String getBuyIncomeResaleVolume() {
        return buyIncomeResaleVolume;
    }

    public void setBuyIncomeResaleVolume(String buyIncomeResaleVolume) {
        this.buyIncomeResaleVolume = buyIncomeResaleVolume;
    }
}
